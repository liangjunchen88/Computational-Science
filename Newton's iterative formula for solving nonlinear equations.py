#牛顿迭代法求解非线性方程组
x1 = [0]*100
x2 = [0]*100
i = 0
x1[0] = -1
x2[0] = 2
while i<= 9:
    x1[i+1] = x1[i] + (x1[i]*x2[i] - 2*(x1[i])**2 + (x2[i])**2 - 3*x2[i] + 5)/(4*x1[i] - x2[i])
    x2[i+1] = x2[i] - (2*(x1[i])**2 + 8*x1[i]*x2[i] - 12*x1[i] - (x2[i])**2 + 5)/(2*(4*x1[i] - x2[i]))
    print(x1[i+1],' ',x2[i+1])
    i += 1